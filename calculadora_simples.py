calculadora = input('Qual operação deseja realizar? Soma[s],Subtração[su],Multiplicação[m] e divisão[d]')
if calculadora == 's':
    numero1 = int(input('Número1: '))
    numero2 = int(input('Número2: '))
    resultado = numero1 + numero2
    print(resultado)
elif calculadora == 'su':
    numero1 = int(input('Número1: '))
    numero2 = int(input('Número2: '))
    resultado = numero1 - numero2
    print(resultado)
elif calculadora == 'm':
    numero1 = int(input('Número1: '))
    numero2 = int(input('Número2: '))
    resultado = numero1 * numero2
    print(resultado)
elif calculadora == 'd':
    numero1 = int(input('Número1: '))
    numero2 = int(input('Número2: '))
    resultado = numero1 / numero2
    print(int(resultado))
    
       
                
        
       
        



